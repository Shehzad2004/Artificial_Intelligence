# Function to calculate sum, maximum, and minimum
def calculate_stats(numbers):
    total_sum = sum(numbers)  # Sum of all numbers
    max_num = max(numbers)    # Maximum value
    min_num = min(numbers)    # Minimum value

    return total_sum, max_num, min_num

# Sample list of numbers
numbers = [10, 20, 4, 56, 23, 98, 34]

# Call the function and store the results
total_sum, max_num, min_num = calculate_stats(numbers)

# Print the results
print(f"Sum: {total_sum}")
print(f"Maximum: {max_num}")
print(f"Minimum: {min_num}")

# Function to add elements to a list
def add_elements_to_list():
    elements = []  # Start with an empty list
    while True:
        # Ask the user to input a value
        element = input("Enter an element to add to the list (or 'done' to stop): ")

        # Check if the user wants to stop
        if element.lower() == 'done':
            break

        # Otherwise, add the element to the list
        elements.append(element)

    return elements

# Call the function and store the result
my_list = add_elements_to_list()

# Print the list after user input
print("Your final list is:", my_list)



