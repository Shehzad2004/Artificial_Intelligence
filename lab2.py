"""# **Sets**"""

# Create two sets with common and unique elements
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# Print the sets
print("Set 1:", set1)
print("Set 2:", set2)

# Common elements (intersection)
common_elements = set1.intersection(set2)
print("Common elements:", common_elements)

# Unique elements in set1 (difference)
unique_to_set1 = set1.difference(set2)
print("Unique to Set 1:", unique_to_set1)

# Unique elements in set2 (difference)
unique_to_set2 = set2.difference(set1)
print("Unique to Set 2:", unique_to_set2)

